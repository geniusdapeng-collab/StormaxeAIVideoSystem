/**
 * 一键生成片头（基于异兽ID）
 * @param {string} beastId - 异兽ID
 * @param {string} titleText - 英文标题
 * @param {Object} options - 选项
 * @returns {Object} - 完整片头结果
 */
function generateOpeningForBeast(beastId, titleText, options = {}) {
  // 自动推荐材质
  const materials = MaterialAdapter.recommendMaterialsForBeast(beastId);

  // 自动推荐动效
  const animationRec = AnimationLibrary.recommendAnimationForBeast(beastId);

  // 自动推荐出品人方式
  const producerMode = ProducerModule.recommendProducerMode(
    options.sceneType || 'default'
  );

  const config = {
    titleText,
    producerText: options.producerText || 'by Genius',
    animationType: options.animationType || animationRec.type,
    animationVariant: options.animationVariant || animationRec.variant,
    producerMode: options.producerMode || producerMode,
    materials: options.materials || materials,
    scene: options.scene,
    beastId,
    duration: options.duration || 9,
    hasXiaoG: options.hasXiaoG !== false
  };

  const titleResult = generateTitlePrompt(config);

  // 🆕 v3.1-Peng: 生成神兽开场声音层描述（Seedance同步生成）
  const audioLayer = VoiceDesigner.generateTitleAudioLayer(beastId);

  return {
    ...titleResult,
    // 🆕 v3.1-Peng: 声音层设计
    audioLayer,
    beastVoiceProfile: {
      name: audioLayer.metadata.beastName,
      estimatedDuration: audioLayer.metadata.estimatedDuration,
      humanReference: audioLayer.metadata.humanReference,
      isAutoGenerated: audioLayer.metadata.isAutoGenerated
    },
    // 时间轴总设计（画面+声音）—— 🆕 v3.3-Peng: 先声夺人模式
    timeline: {
      audio: {
        triggerPoint: '0秒（先声夺人，画面尚未出现）',
        voiceStart: 0.0,     // 声音先开始
        voiceDuration: audioLayer.metadata.estimatedDuration,
        sfxStart: 0.0,       // 音效与声音同步开始
        sfxDuration: audioLayer.metadata.estimatedDuration + 1.5
      },
      visual: {
        start: 0.0,
        sceneEmerges: 0.5,   // 画面开始从黑暗中渐显
        titleReveal: 1.5,      // 主标题完全呈现（声音说到中段）
        subtitleReveal: 2.5,   // 副标题呈现
        producerReveal: 3.0,  // 出品人信息呈现
        beastAppears: audioLayer.metadata.estimatedDuration,  // 神兽视觉出场（声音结束后）
        end: Math.max(config.duration, audioLayer.metadata.estimatedDuration + 1.0)
      }
    },
    version: 'v3.1-Peng'
  };
}

