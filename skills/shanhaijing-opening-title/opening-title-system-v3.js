/**
 * 生成完整片头Prompt
 * @param {Object} config - 配置
 * @returns {Object} - {prompt, lengthCheck, modules, auditInfo}
 */
function generateTitlePrompt(config) {
  let prompt = '';

  // 1. 技术栈声明（0-150字）
  prompt += `【技术规格】虚幻引擎5超写实渲染，电影级光影，16:9横屏，IMAX级视觉冲击。`;
  prompt += `毛孔级纹理，次表面散射SSS，体积雾Volumetric fog，Cinematic lighting。`;
  prompt += `全局光照，光线追踪反射，每一帧细腻到可做壁纸。`;
  prompt += `\n`;

  // 2. Nirath生态场景（150-350字）
  if (config.scene) {
    prompt += `【Nirath生态】${config.scene}\n\n`;
  } else if (config.beastId) {
    // 从异兽ID自动生成场景
    prompt += `【Nirath生态】${generateNirathSceneForBeast(config.beastId)}\n\n`;
  }

  // 3. 材质约束（350-600字）
  if (config.materials && config.materials.length > 0) {
    prompt += `【材质约束】\n`;
    prompt += MaterialAdapter.generateNirathMaterialSection(config.materials);
    prompt += `\n`;
  }

  // 4. 标题动效（600-800字）
  if (config.animationType && config.animationVariant) {
    prompt += AnimationLibrary.generateTitleAnimationPrompt(
      config.animationType,
      config.animationVariant,
      config.titleText,
      { duration: config.duration, hasXiaoG: config.hasXiaoG }
    );
    prompt += `\n`;
  }

  // 5. 出品人信息（800-950字）
  if (config.producerText && config.producerMode) {
    prompt += ProducerModule.generateProducerPrompt(
      config.producerText,
      config.producerMode,
      { hasXiaoG: config.hasXiaoG }
    );
    prompt += `\n`;
  }

  // 6. 角色锚定（强制注入）
  if (config.hasXiaoG !== false) {
    prompt += `${XIAOG_ANCHOR}\n\n`;
  }

  // 7. 合规声明（强制注入）
  prompt += `${COMPLIANCE_DISCLAIMER}\n\n`;

  // 8. 风格强化（950-990字）
  prompt += `【风格】皮克斯迪士尼写实动画级别，超写实3D数字人渲染绝非卡通动漫。`;
  prompt += `色彩冷暖对比Nirath调色盘（蓝紫+琥珀金），双光源照明，光影戏剧感。`;
  prompt += `每一帧艺术品可做壁纸，史诗级视觉冲击。`;

  // 检查字数
  const lengthCheck = MaterialAdapter.checkPromptLength(prompt, 950);

  // 审计信息
  const auditInfo = {
    version: 'v3.0-Peng',
    timestamp: new Date().toISOString(),
    source: 'opening-title-system-v3.js::generateTitlePrompt()',
    titleText: config.titleText,
    modules: {
      material: config.materials?.length > 0,
      animation: !!config.animationType,
      producer: !!config.producerText
    },
    complianceInjected: true,
    xiaoGAnchored: config.hasXiaoG !== false,
    promptLength: prompt.length
  };

  return {
    prompt,
    lengthCheck,
    modules: auditInfo.modules,
    auditInfo
  };
}

