// 生成JSON档案（含部位表）
function generateArchive(beastData) {
  // 构建完整描述文本，用于部位表解析
  const fullDescription = [
    beastData.body,
    beastData.head,
    beastData.eyes,
    beastData.skin,
    beastData.special
  ].filter(Boolean).join('。');
  
  // 使用通用模块生成部位表
  const system = new BeastBodyPartSystem();
  const extracted = system._extractFeaturesFromText(fullDescription);
  
  return {
    id: beastData.id,
    name: beastData.name,
    nameEn: beastData.id.charAt(0).toUpperCase() + beastData.id.slice(1),
    category: beastData.category,
    no: beastData.no,
    origin: '《山海经》',
    appearance: {
      summary: beastData.summary,
      fullDescription: fullDescription,
      body: beastData.body,
      head: beastData.head,
      eyes: beastData.eyes,
      skin: beastData.skin,
      special: beastData.special
    },
    bodyParts: {
      version: 'v1.0-Peng',
      generatedAt: new Date().toISOString(),
      parts: extracted.parts,
      extension: extracted.extension,
      aura: extracted.aura
    },
    abilities: beastData.abilities,
    habitat: {
      location: beastData.habitat,
      climate: '待定',
      features: '待定'
    },
    story: '待定',
    symbolism: '待定',
    visualStyle: '待定',
    cinema: {
      scale: beastData.scale || '大型（十米级）',
      recommendedShots: ['medium', 'wide'],
      lighting: '自然光',
      effects: ''
    }
  };
}

// 主函数
function main() {
  const archiveDir = path.join(__dirname, '..', 'beasts');
  
  // 确保目录存在
  if (!fs.existsSync(archiveDir)) {
    fs.mkdirSync(archiveDir, { recursive: true });
  }
  
  let generated = 0;
  let skipped = 0;
  
  for (const beast of BEASTS_DATA_11_40) {
    const archive = generateArchive(beast);
    const filePath = path.join(archiveDir, `${beast.id}.json`);
    
    // 检查是否已存在
    if (fs.existsSync(filePath)) {
      console.log(`⏭️  跳过已存在: ${beast.name}`);
      skipped++;
      continue;
    }
    
    fs.writeFileSync(filePath, JSON.stringify(archive, null, 2), 'utf-8');
    console.log(`✅ 生成档案: ${beast.name} (${beast.id}.json)`);
    generated++;
  }
  
  console.log(`\n🎉 批量生成完成`);
  console.log(`   新生成: ${generated} 只`);
  console.log(`   跳过已存在: ${skipped} 只`);
  console.log(`   档案目录: ${archiveDir}`);
}

main();